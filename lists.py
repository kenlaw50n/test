# List examples - add multiple values. Anything enclosed in square brackets [] is a list.
# Strings must be encapulated in double quotations "".
l=[10, 20, 30]
print(l)
print(type(l))

l=[10,20,30,"apple","banana","cherry"]
print(l)
print(type(l))

# tuple example = stored are immutable
t=(10,20,30)
print(t)
print(type(t))

# complex data type exmaple
s=4+3j
print(s)
