# Comparative oooperators: < , > , <= , >= , == , !=

# "==" are the numbers equal? (if yes output will be "True", if no output will be "False")
num1=100
num2=200
print(num1 == num2)

# ">" is num1 greater than num2? (if no output will be "False")
num1=100
num2=200
print(num1 > num2)

# "<" is num1 less than num2? (if yes output will be "True")
num1=100
num2=200
print(num1 < num2)

# "<=" is num1 less than or equal to num2? (if yes output will be "True")
num1=100
num2=200
print(num1 <= num2)

# ">=" is num1 greater than or equal to num2? (if no output will be "False")
num1=100
num2=200
print(num1 >= num2)

# "!=" is num1 greater than num2? (if yes output will be "True")
num1=100
num2=200                     
print(num1 != num2)